from mis_functions import exact_mis
import pytest

@pytest.mark.parametrize(
        "input, expected",
        [
            (
                {
                    "1": ["2", "4"],
                    "2": ["1", "5", "3"],
                    "3": ["2", "6"],
                    "4": ["1", "5", "7"],
                    "5": ["2", "4", "6", "8"],
                    "6": ["3", "5", "9"],
                    "7": ["4", "8"],
                    "8": ["7", "5", "9"],
                    "9": ["8", "6"]
                },
                {
                    "1": ["2", "4"],
                    "3": ["2", "6"],
                    "5": ["2", "4", "6", "8"],
                    "7": ["4", "8"],
                    "9": ["8", "6"]
                }
            ),
            (
                {
                    "1": ["6"],
                    "2": ["6"],
                    "3": ["6"],
                    "4": ["6"],
                    "5": ["6"],
                    "6": ["1", "2", "3", "4", "5"]
                },
                {
                    "1": ["6"],
                    "2": ["6"],
                    "3": ["6"],
                    "4": ["6"],
                    "5": ["6"]
                }
            )
        ]
)
def test_exact_mis(input, expected):
    assert exact_mis(input) == expected


